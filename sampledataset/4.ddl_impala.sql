CREATE DATABASE public;

USE public;

CREATE TABLE ORDERS (
	ID BIGINT,
	USER_ID INT,
	PRODUCT_ID INT,
	SUBTOTAL DOUBLE,
	TAX DOUBLE,
	TOTAL DOUBLE,
	DISCOUNT DOUBLE,
	CREATED_AT TIMESTAMP,
	QUANTITY INT
);


CREATE TABLE PEOPLE (
	ID BIGINT,
	ADDRESS STRING,
	EMAIL STRING,
	PASSWORD STRING,
	NAME STRING,
	CITY STRING,
	LONGITUDE DOUBLE,
	STATE STRING,
	SOURCE STRING,
	BIRTH_DATE STRING,
	ZIP STRING,
	LATITUDE DOUBLE,
	CREATED_AT TIMESTAMP
);


CREATE TABLE PRODUCTS (
	ID BIGINT,
	EAN STRING,
	TITLE STRING,
	CATEGORY STRING,
	VENDOR STRING,
	PRICE DOUBLE,
	RATING DOUBLE,
	CREATED_AT TIMESTAMP
);

CREATE TABLE REVIEWS (
	ID BIGINT,
	PRODUCT_ID INT,
	REVIEWER STRING,
	RATING SMALLINT,
	BODY STRING,
	CREATED_AT TIMESTAMP
);
